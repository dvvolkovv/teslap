/// TeslaPay spacing tokens based on an 8 px grid.
abstract final class AppSpacing {
  static const double xxs = 2.0;
  static const double xs = 4.0;
  static const double sm = 8.0;
  static const double md = 16.0;
  static const double lg = 24.0;
  static const double xl = 32.0;
  static const double xxl = 48.0;
  static const double xxxl = 64.0;

  /// Standard horizontal screen margin.
  static const double screenMargin = 16.0;

  /// Card internal padding.
  static const double cardPadding = 16.0;

  /// Minimum list-item height that satisfies touch-target requirements.
  static const double listItemMinHeight = 64.0;

  /// Bottom navigation bar height (without safe area).
  static const double bottomNavHeight = 56.0;

  /// Top app bar height.
  static const double appBarHeight = 56.0;
}

/// TeslaPay border-radius tokens.
abstract final class AppRadius {
  static const double xs = 4.0;
  static const double sm = 8.0;
  static const double md = 12.0;
  static const double lg = 16.0;
  static const double xl = 24.0;
  static const double full = 9999.0;
}
